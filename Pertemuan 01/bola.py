import math
jari_jari = 5

# menghitung luas permukaan bola
luas_permukaan = 4 * math.pi * jari_jari**2

# menghitung volume bola
volume = (4/3) * math.pi * jari_jari**3

print('hasil dari mencari luas bola dengan jari_jari ', jari_jari, ' adalah = ', luas_permukaan)

print('\nhasil dari mencari volume bola dengan jari_jari ', jari_jari, ' adalah = ', volume)
