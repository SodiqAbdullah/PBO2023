import math
jari_jari = 5
tinggi = 10

# Fungsi untuk menghitung luas permukaan tabung
luas_permukaan = 2 * math.pi * jari_jari * (jari_jari + tinggi)

# Fungsi untuk menghitung volume tabung
volume = math.pi * jari_jari**2 * tinggi

print('hasil dari mencari luas tabung dengan\njari_jari : ',jari_jari,'\ntinggi : ',tinggi,'\nadalah = ', luas_permukaan )

print('\nhasil dari mencari volume tabung dengan\njari_jari : ',jari_jari,'\ntinggi : ',tinggi,'\nadalah = ', volume )
